-- =====================================================
-- 1. ATOM CONVERSATIONS TABLE
-- =====================================================
-- Stores full conversation history for replay and resume.
-- Each conversation belongs to a user and a room.
-- Messages stored as JSONB array: [{ role, content, timestamp, agentId? }]

CREATE TABLE IF NOT EXISTS atom_conversations (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,

  -- Conversation context
  room TEXT NOT NULL,                     -- 'desk' | 'library' | 'classroom' | 'training' | 'cbme' | 'community' | 'arena' | 'backstage' | 'studio'
  title TEXT,                             -- Auto-generated summary after first few messages
  topic_slug TEXT,                        -- Topic being discussed (if room is Library/Training)

  -- Message history
  messages JSONB NOT NULL DEFAULT '[]',   -- Array of ATOMMessage objects
  -- Expected shape per message:
  -- {
  --   "role": "user" | "assistant" | "system",
  --   "content": "...",
  --   "timestamp": "2026-02-22T10:00:00Z",
  --   "agentId": "retriever" | null,
  --   "agentLabel": "Retriever" | null
  -- }

  -- Metadata
  metadata JSONB DEFAULT '{}',
  -- Expected shape:
  -- {
  --   "messageCount": 12,
  --   "compactedAt": "2026-02-22T11:00:00Z",
  --   "compactedSummary": "...",
  --   "tokensUsed": 15000,
  --   "lastActiveAgents": ["retriever", "memorist"]
  -- }

  -- Timestamps
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- =====================================================
-- 2. ATOM USER MEMORY TABLE
-- =====================================================
-- Persistent student knowledge state with vector embeddings.
-- The Memorist agent reads before generation and writes after.
-- 7 memory types with per-type decay rates (see decay cron below).

CREATE TABLE IF NOT EXISTS atom_user_memory (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,

  -- Memory classification (7 types — see ATOM_MEMORY_SPEC.md)
  memory_type TEXT NOT NULL CHECK (memory_type IN (
    'topic_mastery',        -- Student has demonstrated understanding
    'weak_area',            -- Student repeatedly struggles with concept
    'preference',           -- Learning style preferences
    'insight',              -- ATOM's observation about learning patterns
    'goal',                 -- Exam targets and ambitions
    'study_pattern',        -- Observed study behavior patterns
    'clinical_connection'   -- Breakthrough learning moments
  )),

  -- Memory content (natural language — human-readable notes about the student)
  content TEXT NOT NULL,

  -- Vector embedding (BGE-small-en-v1.5, 384 dimensions)
  -- Used for semantic search when retrieving relevant memories
  embedding vector(384),

  -- Relevance score (decays daily, resets to 1.0 on access)
  -- Decay rates by type:
  --   topic_mastery: 0.99/day (~69 day half-life)
  --   weak_area: 0.97/day (~23 day half-life)
  --   preference: 0.995/day (~138 day half-life)
  --   insight: 0.97/day (~23 day half-life)
  --   goal: 0.995/day (~138 day half-life)
  --   study_pattern: 0.97/day (~23 day half-life)
  --   clinical_connection: 0.99/day (~69 day half-life)
  relevance_score FLOAT DEFAULT 1.0 CHECK (relevance_score >= 0 AND relevance_score <= 1),

  -- Context: where and when this memory was created
  source_room TEXT,                       -- Room where memory was observed
  topic_slug TEXT,                        -- Related topic (if applicable)

  -- Archival: set when memory decays below threshold
  expires_at TIMESTAMPTZ,

  -- Timestamps
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- =====================================================
-- 3. ATOM PROACTIVE INSIGHTS TABLE
-- =====================================================
-- Heartbeat-driven notifications generated by the Gateway.
-- Checked periodically (every 5 minutes of active use).
-- Delivered via Supabase Realtime → toast notification + widget badge.

CREATE TABLE IF NOT EXISTS atom_proactive_insights (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,

  -- Insight classification (6 types)
  type TEXT NOT NULL CHECK (type IN (
    'study_nudge',          -- "You haven't studied Surgery in 3 days — your weak areas are decaying"
    'spaced_rep_due',       -- "12 retrieval cards are due today — 5 in Pharmacology"
    'streak_alert',         -- "Your 7-day streak is at risk! Study 10 minutes to keep it alive"
    'exam_countdown',       -- "42 days to NEET-PG — here's your priority list"
    'weak_area_alert',      -- "Your Hepatobiliary weak areas are fading from memory — review recommended"
    'memory_insight'        -- "You've mastered 80% of Cardiac Physiology — time to connect to Pharmacology"
  )),

  -- Display content
  title TEXT NOT NULL,                    -- Short headline for toast/card
  message TEXT NOT NULL,                  -- Full insight message
  action_url TEXT,                        -- Deep link: e.g., '/library/surgery/hepatobiliary/cholelithiasis'

  -- State
  is_read BOOLEAN DEFAULT FALSE,
  is_dismissed BOOLEAN DEFAULT FALSE,

  -- Extra data for frontend rendering
  metadata JSONB DEFAULT '{}',
  -- Expected shape:
  -- {
  --   "priority": "high" | "medium" | "low",
  --   "relatedTopics": ["cholelithiasis", "portal-hypertension"],
  --   "dueCards": 12,
  --   "streakDays": 7,
  --   "examDaysRemaining": 42
  -- }

  -- Timestamp
  created_at TIMESTAMPTZ DEFAULT NOW()
);

