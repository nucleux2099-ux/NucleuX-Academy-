-- 4. INDEXES
-- =====================================================

-- atom_conversations indexes
CREATE INDEX IF NOT EXISTS idx_conversations_user
  ON atom_conversations (user_id);

CREATE INDEX IF NOT EXISTS idx_conversations_user_room
  ON atom_conversations (user_id, room);

CREATE INDEX IF NOT EXISTS idx_conversations_updated
  ON atom_conversations (user_id, updated_at DESC);

-- atom_user_memory indexes
CREATE INDEX IF NOT EXISTS idx_memory_user
  ON atom_user_memory (user_id);

-- HNSW index for fast semantic memory search
CREATE INDEX IF NOT EXISTS idx_memory_embedding
  ON atom_user_memory
  USING hnsw (embedding vector_cosine_ops)
  WITH (m = 16, ef_construction = 64);

-- Composite: user + type (for filtered reads like "all weak_areas for user")
CREATE INDEX IF NOT EXISTS idx_memory_user_type
  ON atom_user_memory (user_id, memory_type);

-- Composite: user + source_room (for room-scoped memory reads)
CREATE INDEX IF NOT EXISTS idx_memory_user_room
  ON atom_user_memory (user_id, source_room);

-- Index for relevance-based queries (active memories only)
CREATE INDEX IF NOT EXISTS idx_memory_relevance
  ON atom_user_memory (user_id, relevance_score DESC)
  WHERE relevance_score >= 0.1;

-- atom_proactive_insights indexes
CREATE INDEX IF NOT EXISTS idx_insights_user_unread
  ON atom_proactive_insights (user_id, is_read)
  WHERE is_read = FALSE AND is_dismissed = FALSE;

CREATE INDEX IF NOT EXISTS idx_insights_user_created
  ON atom_proactive_insights (user_id, created_at DESC);

-- =====================================================
-- 5. SEMANTIC MEMORY SEARCH (RPC FUNCTION)
-- =====================================================
-- Used by the Memorist agent during pre-generation read phase.
-- Finds memories semantically similar to the current query.

CREATE OR REPLACE FUNCTION search_memories(
  p_user_id UUID,
  p_embedding vector(384),
  p_limit INTEGER DEFAULT 5,
  p_min_score FLOAT DEFAULT 0.3
)
RETURNS TABLE (
  id UUID,
  memory_type TEXT,
  content TEXT,
  relevance_score FLOAT,
  similarity FLOAT,
  source_room TEXT,
  topic_slug TEXT,
  created_at TIMESTAMPTZ
) AS $$
BEGIN
  RETURN QUERY
  SELECT
    m.id,
    m.memory_type,
    m.content,
    m.relevance_score,
    (1 - (m.embedding <=> p_embedding))::FLOAT AS similarity,
    m.source_room,
    m.topic_slug,
    m.created_at
  FROM atom_user_memory m
  WHERE
    m.user_id = p_user_id
    AND m.embedding IS NOT NULL
    AND m.relevance_score >= 0.1
    AND (1 - (m.embedding <=> p_embedding)) >= p_min_score
  ORDER BY similarity DESC
  LIMIT p_limit;
END;
$$ LANGUAGE plpgsql STABLE SECURITY DEFINER;

-- =====================================================
-- 6. MEMORY DECAY FUNCTION
-- =====================================================
-- Called by a daily cron (Supabase Edge Function or pg_cron).
-- Applies per-type decay rates and archives expired memories.

CREATE OR REPLACE FUNCTION apply_memory_decay()
RETURNS void AS $$
BEGIN
  -- Apply decay based on memory type
  UPDATE atom_user_memory
  SET
    relevance_score = relevance_score * CASE memory_type
      WHEN 'topic_mastery'      THEN 0.99    -- ~69 day half-life
      WHEN 'weak_area'          THEN 0.97    -- ~23 day half-life
      WHEN 'preference'         THEN 0.995   -- ~138 day half-life
      WHEN 'insight'            THEN 0.97    -- ~23 day half-life
      WHEN 'goal'               THEN 0.995   -- ~138 day half-life
      WHEN 'study_pattern'      THEN 0.97    -- ~23 day half-life
      WHEN 'clinical_connection' THEN 0.99   -- ~69 day half-life
      ELSE 0.97
    END,
    updated_at = NOW()
  WHERE relevance_score >= 0.1;

  -- Archive memories that have decayed below threshold
  UPDATE atom_user_memory
  SET
    relevance_score = 0,
    expires_at = NOW(),
    updated_at = NOW()
  WHERE relevance_score < 0.1 AND relevance_score > 0;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- =====================================================
-- 7. CLEANUP FUNCTION FOR PROACTIVE INSIGHTS
-- =====================================================
-- Deletes insights older than 30 days (per data retention policy).

CREATE OR REPLACE FUNCTION cleanup_proactive_insights()
RETURNS void AS $$
BEGIN
  DELETE FROM atom_proactive_insights
  WHERE created_at < NOW() - INTERVAL '30 days';
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

