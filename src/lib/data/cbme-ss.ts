// Helper functions
export function getSSByDegree(degree: SSDegree): SSCurriculum[] {
  return SS_CURRICULA.filter((c) => c.degree === degree);
}

export function getSSByLibrarySubject(librarySubject: string): SSCurriculum[] {
  return SS_CURRICULA.filter((c) => c.librarySubject === librarySubject);
}

export function getSSById(id: string): SSCurriculum | undefined {
  return SS_CURRICULA.find((c) => c.id === id);
}

