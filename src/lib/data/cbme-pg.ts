// Helper functions
export function getPGByDegree(degree: PGDegree): PGCurriculum[] {
  return PG_CURRICULA.filter((c) => c.degree === degree);
}

export function getPGByLibrarySubject(librarySubject: string): PGCurriculum[] {
  return PG_CURRICULA.filter((c) => c.librarySubject === librarySubject);
}

export function getPGById(id: string): PGCurriculum | undefined {
  return PG_CURRICULA.find((c) => c.id === id);
}

export const MD_COUNT = PG_CURRICULA.filter((c) => c.degree === "MD").length;
export const MS_COUNT = PG_CURRICULA.filter((c) => c.degree === "MS").length;
